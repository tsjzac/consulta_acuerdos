// Script para el menú móvil (simplificado, ya que no tenemos jQuery ni AOS)
document.addEventListener('DOMContentLoaded', function() {
    const mobileNavToggle = document.querySelector('.mobile-nav-toggle');
    const mobileNav = document.querySelector('.mobile-nav');
    const mobileNavOverly = document.querySelector('.mobile-nav-overly');

    if (mobileNavToggle) {
        mobileNavToggle.addEventListener('click', function() {
            document.body.classList.toggle('mobile-nav-active');
            mobileNavToggle.querySelector('i').classList.toggle('bx-menu');
            mobileNavToggle.querySelector('i').classList.toggle('bx-x');
            if (mobileNavOverly.style.display === 'block') {
                mobileNavOverly.style.display = 'none';
            } else {
                mobileNavOverly.style.display = 'block';
            }
        });
    }

    // Manejo de submenús en móvil
    const dropDowns = document.querySelectorAll('.mobile-nav .drop-down > a');
    dropDowns.forEach(dropDown => {
        dropDown.addEventListener('click', function(e) {
            e.preventDefault();
            const nextUl = this.nextElementSibling;
            if (nextUl && nextUl.tagName === 'UL') {
                if (nextUl.style.display === 'block') {
                    nextUl.style.display = 'none';
                    this.parentElement.classList.remove('active');
                } else {
                    nextUl.style.display = 'block';
                    this.parentElement.classList.add('active');
                }
            }
        });
    });

    // Cerrar menú móvil al hacer clic en un elemento "cerrar"
    const cerrarItems = document.querySelectorAll('.mobile-nav .cerrar');
    cerrarItems.forEach(item => {
        item.addEventListener('click', function() {
            document.body.classList.remove('mobile-nav-active');
            mobileNavToggle.querySelector('i').classList.remove('bx-x');
            mobileNavToggle.querySelector('i').classList.add('bx-menu');
            mobileNavOverly.style.display = 'none';
        });
    });

    // Cerrar menú móvil al hacer clic fuera
    document.addEventListener('click', function(e) {
        if (!mobileNav.contains(e.target) && !mobileNavToggle.contains(e.target)) {
            if (document.body.classList.contains('mobile-nav-active')) {
                document.body.classList.remove('mobile-nav-active');
                mobileNavToggle.querySelector('i').classList.remove('bx-x');
                mobileNavToggle.querySelector('i').classList.add('bx-menu');
                mobileNavOverly.style.display = 'none';
            }
        }
    });

    // Back to top button (simplificado)
    const backToTop = document.querySelector('.back-to-top');
    if (backToTop) {
        window.addEventListener('scroll', () => {
            if (window.scrollY > 100) {
                backToTop.style.display = 'block';
            } else {
                backToTop.style.display = 'none';
            }
        });

        backToTop.addEventListener('click', (e) => {
            e.preventDefault();
            window.scrollTo({
                top: 0,
                behavior: 'smooth'
            });
        });
    }
});
